    public void Save() {
        try {
            PR.PROVEEDORES(Integer.parseInt(Txt_codigo.getText()),
                         Txt_razonsocial.getText(),
                        Txt_direccion.getText(),
                        Txt_contacto.getText(),
                        Txt_ciudad.getText(),
                        Txt_telefono.getText(),
                        Txt_celular.getText());
            PR.INSERT_PROVEEDORES(CN.getConnection());
            formWindowOpened(null);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    public void Update() {
        try {
            PR.PROVEEDORES(Integer.parseInt(Txt_codigo.getText()),
                        Txt_razonsocial.getText(),
                        Txt_direccion.getText(),
                        Txt_contacto.getText(),
                        Txt_ciudad.getText(),
                        Txt_telefono.getText(),
                        Txt_celular.getText());
            PR.UPDATE_PROVEEDORES(CN.getConnection(), id);
            formWindowOpened(null);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    public void Delete(){
        try {
            PR.DELETE_PROVEEDORES(CN.getConnection(), id);
            formWindowOpened(null);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }
    
